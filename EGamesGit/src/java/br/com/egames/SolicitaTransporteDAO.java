package br.com.egames;

/**
 *
 * @author Leandro
 */
public class SolicitaTransporteDAO {
    private String cnpjTranp;
    private String idNota;
    private String idPedido;
    private String idTelefone;

    public String getCnpjTranp() {
        return cnpjTranp;
    }

    public void setCnpjTranp(String cnpjTranp) {
        this.cnpjTranp = cnpjTranp;
    }

    public String getIdNota() {
        return idNota;
    }

    public void setIdNota(String idNota) {
        this.idNota = idNota;
    }

    public String getIdPedido() {
        return idPedido;
    }

    public void setIdPedido(String idPedido) {
        this.idPedido = idPedido;
    }

    public String getIdTelefone() {
        return idTelefone;
    }

    public void setIdTelefone(String idTelefone) {
        this.idTelefone = idTelefone;
    }
    
    
}
